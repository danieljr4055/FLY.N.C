function calcularSimilitud(p1, p2) {
  const palabras1 = new Set(limpiarTexto(p1).split(" "));
  const palabras2 = new Set(limpiarTexto(p2).split(" "));

  // Revisar palabras clave
  for (const categoria in palabrasClave) {
    const claves = palabrasClave[categoria];
    const tieneClaveP1 = claves.some(c => [...palabras1].some(p => p.includes(c)));
    const tieneClaveP2 = claves.some(c => [...palabras2].some(p => p.includes(c)));
    if (tieneClaveP1 && tieneClaveP2) {
      return 0.8; // Alta similitud si coinciden palabras clave
    }
  }

  // CÃ¡lculo normal de similitud si no hay palabras clave coincidentes
  const interseccion = [...palabras1].filter(p => palabras2.has(p)).length;
  const union = Math.max(palabras1.size, palabras2.size);
  return interseccion / union;
}

